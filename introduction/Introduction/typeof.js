//Checking Data Types using typeof and comments

console.log(typeof 'Magenta') // console output is string.
console.log(typeof 7) // console output is number.
console.log(typeof true) // console output is boolean.
console.log(typeof null) // console output is object.
console.log(typeof undefined) // console output is undefined.

